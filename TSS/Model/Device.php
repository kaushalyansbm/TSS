<?php

class Device{
   
private $serialnu;
private $typee;
private $brand;
private $remark;
private $model;
private $warenty;


function setSerialnu($serialnu) { $this->serialnu = $serialnu; }
function getSerialnu() { return $this->serialnu; }
function setTypee($typee) { $this->typee = $typee; }
function getTypee() { return $this->typee; }
function setBrand($brand) { $this->brand = $brand; }
function getBrand() { return $this->brand; }
function setRemark($remark) { $this->remark = $remark; }
function getRemark() { return $this->remark; }
function setModel($model) { $this->model = $model; }
function getModel() { return $this->model; }
function setWarenty($warenty) { $this->warenty = $warenty; }
function getWarenty() { return $this->warenty; }




}


