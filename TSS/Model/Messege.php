<?php


class Messege{

private $id;
private $subject;
private $content;
private $sendtime;
private $sendDate;

function setId($id) { $this->id = $id; }
function getId() { return $this->id; }
function setSubject($subject) { $this->subject = $subject; }
function getSubject() { return $this->subject; }
function setContent($content) { $this->content = $content; }
function getContent() { return $this->content; }
function setSendtime($sendtime) { $this->sendtime = $sendtime; }
function getSendtime() { return $this->sendtime; }
function setSendDate($sendDate) { $this->sendDate = $sendDate; }
function getSendDate() { return $this->sendDate; }






	
}